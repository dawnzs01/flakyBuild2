package pkg;

public class TestReturnIf {
    boolean testEq(int x) {
        return x == 1;
    }

    boolean testL(int x) {
        return x < 1;
    }

    boolean testG(int x) {
        return x > 1;
    }

    boolean testLEq(int x) {
        return x <= 1;
    }

    boolean testGEq(int x) {
        return x >= 1;
    }

    boolean testNEq(int x) {
        return x != 1;
    }
}
